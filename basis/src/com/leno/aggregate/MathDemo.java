public class MathDemo {
    public static void main(String[] args) {
        //取绝对值
        System.out.println(Math.abs(-1));
        //开2次方
        System.out.println(Math.sqrt(2));
        //向上取整
        System.out.println(Math.ceil(3.113));
        //向下取整
        System.out.println(Math.floor(3.113));
    }
}
